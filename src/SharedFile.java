public class SharedFile {

    private int id;
    private int size;
    private int uploadedSize;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public int getUploadedSize() {
        return uploadedSize;
    }

    public void setUploadedSize(int uploadedSize) {
        this.uploadedSize = uploadedSize;
    }
}
